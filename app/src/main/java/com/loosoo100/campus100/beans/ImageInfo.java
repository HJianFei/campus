package com.loosoo100.campus100.beans;

/**
 * 
 * @author yang 图片信息
 * 
 */
public class ImageInfo {
	// 图片地址
	private String url;
	// 跳转地址
	private String goUrl;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getGoUrl() {
		return goUrl;
	}

	public void setGoUrl(String goUrl) {
		this.goUrl = goUrl;
	}

}
