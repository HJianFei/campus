package com.loosoo100.campus100.beans;

/**
 * 
 * @author yang
 * 商品规格属性
 *
 */
public class GiftGoodsAttrVal {
	// 属性编号
	private int attrSymbol;
	// 属性值
	private String attrValue;
	// 图片
	private String attrPic;

	public int getAttrSymbol() {
		return attrSymbol;
	}

	public void setAttrSymbol(int attrSymbol) {
		this.attrSymbol = attrSymbol;
	}

	public String getAttrValue() {
		return attrValue;
	}

	public void setAttrValue(String attrValue) {
		this.attrValue = attrValue;
	}

	public String getAttrPic() {
		return attrPic;
	}

	public void setAttrPic(String attrPic) {
		this.attrPic = attrPic;
	}
}
